function generateStudentObjects(age, studentClass, count, arrayNames) {
  const studentsArray = [];
  const usedNames = new Set();

  function getRandomAge() {
    // Generate a random age (either the provided age or one less)
    return age - (Math.random() < 0.5 ? 1 : 0);
  }

  function getRandomDob(randomAge) {
    // Generate a random birth year based on the age
    const currentYear = new Date().getFullYear();
    const randomBirthYear = currentYear - randomAge;

    // Generate a random birth month (between 1 and 12)
    const randomMonth = Math.floor(Math.random() * 12) + 1;

    // Generate a random birth day (between 1 and 28, assuming February has 28 days)
    const randomDay = Math.floor(Math.random() * 28) + 1;

    return `${randomBirthYear}-${randomMonth
      .toString()
      .padStart(2, "0")}-${randomDay.toString().padStart(2, "0")}`;
  }

  function getRandomGender(name) {
    // Determine gender based on the name (a simple heuristic)
    return name.endsWith("a") ? "Female" : "Male";
  }

  function getRandomCarsScore() {
    // Generate a random carsScore between 4.00 and 9.36 with two decimal places
    return (Math.random() * (9.36 - 4.0) + 4.0).toFixed(2);
  }

  function generateUniqueStudentId() {
    // Generate a unique studentId
    let studentId;
    do {
      studentId = Math.floor(Math.random() * 90000000 + 10000000).toString(); // Generates an 8-digit unique ID
    } while (usedNames.has(studentId));
    return studentId;
  }

  for (let i = 0; i < count; i++) {
    // Randomly select a name from the arrayNames
    const randomNameIndex = Math.floor(Math.random() * arrayNames.length);
    const name = arrayNames.splice(randomNameIndex, 1)[0];
    usedNames.add(name);

    // Generate student object with the specified properties
    const randomAge = getRandomAge();
    const dob = getRandomDob(randomAge);
    const gender = getRandomGender(name);
    const carsScore = getRandomCarsScore();
    const studentId = generateUniqueStudentId();

    const student = {
      name: name,
      class: studentClass,
      studentId: studentId,
      dob: dob,
      gender: gender,
      age: randomAge,
      carsScore: carsScore,
    };

    studentsArray.push(student);
  }

  // return studentsArray;
  console.log(studentsArray);
}

