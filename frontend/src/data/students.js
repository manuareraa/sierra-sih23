function updateAgeAndDOB(studentsArray) {
  const currentDate = new Date("2023-12-15");

  studentsArray.forEach((student) => {
    // Generate a random age between 9 and 10
    const randomAge = Math.floor(Math.random() * 2) + 9;

    // Update the "age" key with the random age
    student.age = randomAge;

    // Calculate the birth year based on the age
    const birthYear = currentDate.getFullYear() - randomAge;

    // Generate a random birth month (between 1 and 12)
    const randomMonth = Math.floor(Math.random() * 12) + 1;

    // Generate a random birth day (between 1 and 28, assuming February has 28 days)
    const randomDay = Math.floor(Math.random() * 28) + 1;

    // Create the date of birth string with slight randomization
    const dob = `${birthYear}-${(randomMonth + Math.floor(Math.random() * 3))
      .toString()
      .padStart(2, "0")}-${(randomDay + Math.floor(Math.random() * 3))
      .toString()
      .padStart(2, "0")}`;

    // Update the "dob" key with the modified date of birth
    student.dob = dob;
  });

  console.log(studentsArray);
}

